declare(strict_types=1);

namespace OmegaCode\JwtSecuredApiCore\Middleware;

use Psr\Http\Message\ResponseInterface;
use Psr\Http\Message\ServerRequestInterface;
use Psr\Http\Server\MiddlewareInterface;
use Psr\Http\Server\RequestHandlerInterface;

class CORSMiddleware implements MiddlewareInterface
{
    public function process(ServerRequestInterface $request, RequestHandlerInterface $handler): ResponseInterface
    {
        $response = $handler->handle($request);

        return $response
            ->withHeader('Access-Control-Allow-Origin', $request->getHeader('origin'))
            ->withHeader('Access-Control-Allow-Credentials', $_ENV['CORS_ALLOW_CREDENTIALS'] ?? 'false')
            ->withHeader(
                'Access-Control-Allow-Headers',
                $_ENV['CORS_HEADERS'] ?? 'X-Requested-With, Content-Type, Accept, Origin, Authorization'
            )
            ->withHeader(
                'Access-Control-Allow-Methods',
                $_ENV['CORS_METHODS'] ?? 'GET, POST, PUT, DELETE, PATCH, OPTIONS'
            );
    }
}
